#include "../include/libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	unsigned char		*dest_ptr;
	const unsigned char	*src_ptr;
	size_t				i;

	dest_ptr = dest;
	src_ptr = src;
	if (!dest_ptr || !src_ptr)
		return (dest);
	i = -1;
	while (++i < n)
		dest_ptr[i] = src_ptr[i];
	return (dest);
}
